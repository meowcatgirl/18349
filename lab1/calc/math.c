#include "math.h"

// Returns addition of x and y.
int add (int x, int y) {
	return x+y;
}

// Returns subtraction x-y.
int sub (int x, int y) {
	return x-y;
}

// Returns multiplication of x and y.
int mul (int x, int y) {
	return x*y;
}

// Returns division of x by y.
int cdiv (int x, int y) {
	return x/y;
}

// Returns mod of x by y.
int mod (int x, int y) {
	return x%y;
}
